    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String uname = "";
        uname = jTextField1.getText();
        char[] passwd = jPasswordField1.getPassword();
        String password = new String(passwd);
        Connection con = DBConnection.getConnection();
        if(uname.equals(""))
        {
            jLabel5.setText("Please Enter Username !!!");
        }
        else
        {
            ResultSet rs = null;
            String sql = "select * from login where username = '"+uname+"'";
            try
            {
                rs=(ResultSet) con.createStatement().executeQuery(sql);
                if(rs.next())
                {
                    if(rs.getString(3).equals(password))
                    {
                          this.setVisible(false);
                          EnterAmount ent = new EnterAmount(uname);
                          ent.setVisible(true);
                    }
                    else
                    {
                        jLabel5.setText("Invalid Password !!!");
                    }
                }
                else
                {
                    jLabel5.setText("User does not exist !!!");
                }
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

